        <h2>Capacitación Ambiental:</h2>
        <p>Ofrecemos talleres educativos sobre la gestión sostenible de residuos y la implementación de prácticas ecológicas en comunidades y organizaciones. Nuestras capacitaciones están diseñadas para empoderar a los participantes con conocimientos y habilidades en compostaje, vermicompostaje, huertos urbanos, y la creación de soluciones medioambientales efectivas.</p>
        <h3>Talleres de Compostaje y Vermicompostaje:</h3>
        <p>Enseñamos a convertir los residuos orgánicos en compost y humus de alta calidad mediante métodos naturales. Nuestros talleres incluyen teoría y práctica, capacitando a los participantes para implementar técnicas de compostaje y vermicompostaje en sus hogares, escuelas o comunidades, reduciendo así la cantidad de residuos y mejorando la fertilidad del suelo.</p>
        <h4>Consultoría en Proyectos Verdes:</h4>
        <p>Asesoramos a empresas, comunidades y gobiernos en la planificación e implementación de proyectos verdes que promuevan la sostenibilidad y la economía circular. Ayudamos a diseñar e implementar programas de compostaje, gestión de residuos, huertos urbanos, y otras iniciativas ecológicas adaptadas a las necesidades locales.</p>
