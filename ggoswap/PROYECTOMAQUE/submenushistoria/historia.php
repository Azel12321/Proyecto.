        <h1>¿ QUIENES SOMOS ?</h1>
        <p>Es un emprendimiento que ofrece la oportunidad de formar parte de un ecosistema circular y ambiental mediante el uso de la tecnología, para todo público , ya sea al sector empresarial e industrial, como al sector agro y ciudadano de las diferentes comunidades y departamentos. Buscando la sostenibilidad con: la comercialización de abonos orgánicos , alimentos sembrados y sub-productos , la gestión e implementación del servicio de recolección diferenciado (Fracción orgánica) , alianzas estratégicas entre el sector público y privado promoviendo el cuidado de la salud pública y medio ambiente Este proyecto busca aprovechar los residuos orgánicos, que paran en la basura afectando directamente la salud publica y ecosistemas naturales. Donde mas del 50% de basura son residuos orgánicos. Estos residuos orgánicos se pueden transformar y aprovechar para energias alternativas , en biofertilizantes mediante procesos de compostaje para enriquecer los suelos, , desarrollar cultivos y huertos orgánicos. Así se promueve a una alimentación y agricultura mas sana y responsable para la salud de todos</p>
